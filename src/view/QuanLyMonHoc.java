    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        //Xóa Môn Học : 
        for (int i = 0; i < listMonHoc.size(); i++) {
            if (listMonHoc.get(i).getTenMonHoc().equals(selectedMonHoc.getTenMonHoc())) {
                txtGioiThieu.setText("");
                txtMaHocPhan.setText("");
                txtTenMonHoc.setText("");
                spSoChuong.setValue(1);
                modelListMonHoc.removeElement(selectedMonHoc);
                jListDanhSachMonHoc.setModel(modelListMonHoc);
                listMonHoc.remove(listMonHoc.get(i));
                writeListMonHoc();
                JOptionPane.showMessageDialog(null, "Đã Xóa Thành Công!");
                break;
            }
        }
        btnCapNhat.setEnabled(false);
        btnXoa.setEnabled(false);
    }//GEN-LAST:event_btnXoaActionPerformed

