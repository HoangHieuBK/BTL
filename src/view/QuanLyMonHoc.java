    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // Lưu Môn Học và show lên list
        if (txtTenMonHoc.getText().isEmpty() || txtMaHocPhan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nhập tên môn học và mã học phần!");
        } 
        else if (!kiemTraTenvaMaHocPhan()) {
            for (MonHoc monHoc : listMonHoc) {
                if (monHoc.getTenMonHoc().equals(txtTenMonHoc.getText().trim()) && monHoc.getMaHocPhan().equals(txtMaHocPhan.getText().trim())) {
                    monHoc.setGioiThieu(txtGioiThieu.getText());
                    monHoc.setSoChuong((int) spSoChuong.getValue());
                }
            } 
        } 
        else {
            MonHoc monHoc = new MonHoc();
            monHoc.setTenMonHoc(txtTenMonHoc.getText());
            monHoc.setSoChuong((int) spSoChuong.getValue());
            monHoc.setGioiThieu(txtGioiThieu.getText());
            monHoc.setMaHocPhan(txtMaHocPhan.getText());
            monHoc.setDsCauHoiTL(new ArrayList<>());
            monHoc.setDsCauHoiTN(new ArrayList<>());
            monHoc.setDsDeThi(new ArrayList<>());
            modelListMonHoc.addElement(monHoc);
            jListDanhSachMonHoc.setModel(modelListMonHoc);
            listMonHoc.add(monHoc);
            
        }
        writeListMonHoc();
        txtTenMonHoc.setEnabled(false);
        txtMaHocPhan.setEnabled(false);
        txtGioiThieu.setEnabled(false);
        spSoChuong.setEnabled(false);
        btnLuu.setEnabled(false);
        btnCapNhat.setEnabled(false);
        btnXoa.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Đã lưu Thành Công!");
    }//GEN-LAST:event_btnLuuActionPerformed

